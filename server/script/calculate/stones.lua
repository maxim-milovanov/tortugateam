------
-- Узнаем максимальный уровень самоцвета по идентификатору
------
function GetStoneMaxLv(item_id)
	local max_level = 4
	
	for pos, value in pairs(STONE) do
		if (value.item_id == item_id) then
			max_level = value.max_lv
			break
		end
	end
	
	return max_level
end

------
-- Конвертируем описание в типы предметов, в которые можно ковать необходимый самоцвет
------
function getStoneItemType(stone_id)
	-- Узнаем описание предмета
	local get_item_description = GetItemDescription(stone_id)
	-- Если итем изначально кривой без описания
	if (get_item_description == '0') then
		-- Завершаем сценарий
		return false
	end
	
	-- Объявляем таблицу
	local tables = {}
	-- Парсим описание, через запятую должны находиться типы эквипа
	local _exp = explode(',', get_item_description)
	
	-- Циклом идем по массиву (_exp)
	for _, value in pairs(_exp) do
		-- На всякий случай преобразуем в число
		value = tonumber(value)
		-- Добавляем наш тип предмета в массив
		table.insert(tables, value)
	end
	
	-- Добавим к нашей таблице нолик в конце, по дефолту везде нолик юзается
	table.insert(tables, 0)
	
	-- Сценарий завершается возвращением таблицы
	return tables
end

------
-- Возвращает идентификатор-позицию самоцвета в таблице @STONE
-- @see ItemUse_Rent65()
------
function stonePos(stone_id)
	local pos = 0
	for i, value in pairs(STONE) do
		if (value.item_id == stone_id) then
			pos = i
			break
		end
	end
	
	return pos
end